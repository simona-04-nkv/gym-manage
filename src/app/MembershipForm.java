    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        int row = jTable1.getSelectedRow();
if (row == -1) {
    JOptionPane.showMessageDialog(this, "Choose row you have created.");
    return;
}

try {
    
    int memberId = Integer.parseInt(jTable1.getValueAt(row, 1).toString());
    int planId = Integer.parseInt(jTable1.getValueAt(row, 2).toString());
    String startDate = jTable1.getValueAt(row, 3).toString();
    String endDate = jTable1.getValueAt(row, 4).toString();
    String status = jTable1.getValueAt(row, 5).toString();
    double paid = Double.parseDouble(jTable1.getValueAt(row, 6).toString());
    
    for (int i = 0; i < jTable1.getRowCount(); i++) {
    if (i == row) continue;
    
    int n = Integer.parseInt(jTable1.getValueAt(i, 1).toString());

    if (memberId == n) {
        JOptionPane.showMessageDialog(this,
            "This person exists in the database");
        return;
    }
}

    Membership m = new Membership(memberId, planId, startDate, endDate, status, paid);
    membershipRepo.insert(m);

        JOptionPane.showMessageDialog(this, "Successfully added.");
        
} catch (Exception e) {
    JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
}
    }//GEN-LAST:event_jButton2ActionPerformed

